x = 10
y = 5

#1. equal/sama dengan ==
print(f"{x} = {y} : {x==y}")

#2. not equal ()
print(f"{x} != {y} : {x!=y}")

#3. Greater than >
print(f"{x} > {y} : {x>y}")

#4 Less than <
print(f"{x} < {y} : {x<y}")

#5. Greater than or Equal to >=
print(f"{x} >= {y} : {x>=y}")

#6. Less than or equal to
print(f"{x} <= {y} : {x<=y}")


